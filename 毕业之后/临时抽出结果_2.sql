          , PT02.NKIENTYMD
          , PT02.MAIL
          , PT02.KTIMAIL
          , PT02.TKIYMD
          , PT02.TKIST
          , PT02.KAIKANNO
        FROM
				ipossch.PT01
	      INNER JOIN ipossch.PT02
	 		 ON PT02.KANKAISHACD = PT01.KANKAISHACD
	 		AND PT02.DAIKANNO = PT01.DAIKANNO
	 	  INNER JOIN ipossch.PT03
	 	     ON PT03.KANKAISHACD = PT02.KANKAISHACD
	 	    AND PT03.KAIKANNO = PT02.KAIKANNO
	 	  INNER JOIN ipossch.PT10
	 	     ON PT10.KANKAISHACD = PT03.KANKAISHACD
	 	    AND PT10.CARDSYUCD = PT03.CARDSYUCD
	 	  INNER JOIN ipossch.PT15
	 	     ON PT15.KANKAISHACD = PT02.KANKAISHACD
	 	    AND PT15.KAISHACD = PT02.NKIKAISHACD
	 	    AND PT15.TENCD = PT02.NKITENCD
       WHERE
               PT02.KAIKANNO = '99'
            AND PT01.KANKAISHACD = '01'
            AND PT03.DELFLG = '0'
            AND PT02.DELFLG = '0'
            AND PT01.DELFLG = '0'
            AND PT10.DELFLG = '0'
            AND PT15.DELFLG = '0') T
##T是临时产生的抽出结果
      ORDER BY DAIKANNO ASC, KAIKANNO ASC , CARDNO ASC

